int	ft_sqrt(int nb)
{
	int	i;
	int	sqrt;

	i = 1;
	sqrt = 0;
	if (nb > 0)
	{
		while (nb > 0)
		{
			nb -= i;
			sqrt++;
			i += 2;
		}
		if (nb == 0)
			return (sqrt);
	}
	return (0);
}
